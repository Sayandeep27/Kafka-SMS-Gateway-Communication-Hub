IF OBJECT_ID('dbo.TBL_VENDORHEALTH_DETAILS', 'U') IS NOT NULL
    DROP TABLE dbo.TBL_VENDORHEALTH_DETAILS;
IF OBJECT_ID('dbo.TBL_VENDOR_HEALTH_CONFIG', 'U') IS NOT NULL
    DROP TABLE dbo.TBL_VENDOR_HEALTH_CONFIG;

CREATE TABLE dbo.TBL_VENDORHEALTH_DETAILS (
    ID BIGINT IDENTITY(1,1) PRIMARY KEY,
    VENDOR_CD NVARCHAR(100) NOT NULL,
    HEALTHSTATUS NVARCHAR(20) NOT NULL,
    CONSECUTIVEERRORCOUNT INT NOT NULL DEFAULT 0,
    CONSECUTIVESUCCESSCOUNT INT NOT NULL DEFAULT 0,
    LastFailureTime DATETIME2 NULL,
    LastSuccessTime DATETIME2 NULL,
    DownSince DATETIME2 NULL,
    UpdatedOn DATETIME2 NULL
);

CREATE TABLE dbo.TBL_VENDOR_HEALTH_CONFIG (
    ID BIGINT IDENTITY(1,1) PRIMARY KEY,
    FAILURETHRESHOLDCOUNT INT NOT NULL,
    SUCCESSTHRESHOLDCOUNT INT NOT NULL,
    HEALTHCHECKINTERVAL_MIN INT NOT NULL,
    IS_HEALTHCHECK_ENABLED BIT NOT NULL DEFAULT 1,
    CREATED_BY NVARCHAR(100) NULL,
    CREATED_ON DATETIME2 NULL,
    UPDATED_BY NVARCHAR(100) NULL,
    UPDATED_ON DATETIME2 NULL
);

IF OBJECT_ID('dbo.USP_HEALTHCHECK_CONFIG_DETAILS', 'P') IS NOT NULL
    DROP PROCEDURE dbo.USP_HEALTHCHECK_CONFIG_DETAILS;
IF OBJECT_ID('dbo.USP_VENDOR_HEALTH_MARK_DOWN', 'P') IS NOT NULL
    DROP PROCEDURE dbo.USP_VENDOR_HEALTH_MARK_DOWN;
IF OBJECT_ID('dbo.USP_VENDOR_HEALTH_MARK_UP', 'P') IS NOT NULL
    DROP PROCEDURE dbo.USP_VENDOR_HEALTH_MARK_UP;

EXEC('CREATE PROCEDURE dbo.USP_HEALTHCHECK_CONFIG_DETAILS
    @FLAG VARCHAR(50)
AS
BEGIN
    SET NOCOUNT ON;

    IF @FLAG = ''GET_CONFIG_DATA''
        SELECT TOP 1 ID, FAILURETHRESHOLDCOUNT, SUCCESSTHRESHOLDCOUNT, HEALTHCHECKINTERVAL_MIN, IS_HEALTHCHECK_ENABLED
        FROM dbo.TBL_VENDOR_HEALTH_CONFIG WITH (NOLOCK);

    IF @FLAG = ''GET_VENDOR_HEALTH_DATA''
        SELECT ID, VENDOR_CD, HEALTHSTATUS, CONSECUTIVEERRORCOUNT, LastFailureTime, LastSuccessTime, DownSince, UpdatedOn, CONSECUTIVESUCCESSCOUNT
        FROM dbo.TBL_VENDORHEALTH_DETAILS WITH (NOLOCK)
        WHERE HEALTHSTATUS = ''DOWN'';
END');

EXEC('CREATE PROCEDURE dbo.USP_VENDOR_HEALTH_MARK_DOWN
    @ID BIGINT,
    @DOWN_SINCE DATETIME2 = NULL
AS
BEGIN
    SET NOCOUNT ON;
    UPDATE dbo.TBL_VENDORHEALTH_DETAILS
    SET HEALTHSTATUS = ''DOWN'',
        CONSECUTIVEERRORCOUNT = 0,
        CONSECUTIVESUCCESSCOUNT = 0,
        LastFailureTime = SYSUTCDATETIME(),
        DownSince = COALESCE(@DOWN_SINCE, SYSUTCDATETIME()),
        UpdatedOn = SYSUTCDATETIME()
    WHERE ID = @ID;
END');

EXEC('CREATE PROCEDURE dbo.USP_VENDOR_HEALTH_MARK_UP
    @ID BIGINT
AS
BEGIN
    SET NOCOUNT ON;
    UPDATE dbo.TBL_VENDORHEALTH_DETAILS
    SET HEALTHSTATUS = ''UP'',
        CONSECUTIVEERRORCOUNT = 0,
        CONSECUTIVESUCCESSCOUNT = 0,
        LastSuccessTime = SYSUTCDATETIME(),
        DownSince = NULL,
        UpdatedOn = SYSUTCDATETIME()
    WHERE ID = @ID;
END');
